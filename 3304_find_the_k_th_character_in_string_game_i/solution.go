package main

import "math/bits"

func kthCharacter(k int) byte {
    ans := 0               // Счётчик сдвигов
    for k != 1 {           // Пока не дойдём до базового случая ('a')
        t := bits.Len(uint(k)) - 1  // Находим ближайшую степень двойки ≤ k
        if 1<<t == k {     // Если k — степень двойки
            t--            // Берём предыдущую степень (важно для корректного вычитания)
        }
        k -= 1 << t        // Переходим к соответствующей позиции в предыдущей строке
        ans++              // Увеличиваем счётчик сдвигов
    }
    return byte('a' + ans) // Возвращаем символ с учётом сдвигов
}